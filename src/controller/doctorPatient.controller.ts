import { Request, Response } from "express";
import { doctorPatientRepository } from "../db/repository";

class DoctorPatientController {
    async findAll(_req: Request, res: Response) {
        try {
            const result = await doctorPatientRepository.findAll();
            res.json(result);
        } catch (error) {
            res.status(500).json(error);
        }
    }

    async findById(req: Request, res: Response) {
        try {
            const id = String(req.params.id);
            const result = await doctorPatientRepository.findById(id);
            res.json(result);
        } catch (error) {
            res.status(500).json(error);
        }
    }

    async create(req: Request, res: Response) {
        try {
            const result = await doctorPatientRepository.create(req.body);
            res.json(result);
        } catch (error) {
            res.status(500).json(error);
        }
    }

    async update(req: Request, res: Response) {
        try {
            const id = String(req.params.id);
            const result = await doctorPatientRepository.update(id, req.body);
            res.json(result);
        } catch (error) {
            res.status(500).json(error);
        }
    }

    async delete(req: Request, res: Response) {
        try {
            const id = String(req.params.id);
            await doctorPatientRepository.delete(id);
            res.json({ message: "Deleted successfully" });
        } catch (error) {
            res.status(500).json(error);
        }
    }
}

export const doctorPatientController = new DoctorPatientController();